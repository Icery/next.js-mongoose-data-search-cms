import { useMemo } from 'react';

import { useMutation } from '@tanstack/react-query';

import { UserLoginDto, UserRegisterDto, UserResendVerificationDto, UserUpdateDto } from '@/domains/user';
import { userLogin, userRegister, userResendVerification, userUpdate } from '@/services/auth';
import { UserLoginReturnType, UserResendVerificationReturnType, UserUpdateReturnType } from '@/services/interfaces';
import type { UseMutationFn } from '@/utils/reactQuery';

export const useUserRegisterMutation: UseMutationFn<UserLoginReturnType, UserRegisterDto> = (args) => {
  const { onError, onSuccess, mutationPrefixKey = [] } = args ?? {};
  const {
    isPending: isLoading,
    isError,
    error,
    data,
    mutate,
    mutateAsync,
  } = useMutation({
    mutationKey: [...mutationPrefixKey],
    mutationFn: userRegister,
    onSuccess,
    onError,
  });

  return useMemo(() => {
    return { isLoading, isError, error, data, mutate, mutateAsync };
  }, [isLoading, isError, error, data, mutate, mutateAsync]);
};

