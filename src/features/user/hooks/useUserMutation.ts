import { useMemo } from 'react';

import { useMutation } from '@tanstack/react-query';

import { GetUserDto, UserLoginDto, UserResendVerificationDto, UserUpdateDto } from '@/domains/user';
import { userLogin, userResendVerification, userUpdate } from '@/services/auth';
import {
  GetUserReturnType,
  UserLoginReturnType,
  UserResendVerificationReturnType,
  UserUpdateReturnType,
} from '@/services/interfaces';
import { getUser } from '@/services/user';
import type { UseMutationFn } from '@/utils/reactQuery';

export const useUserMutation: UseMutationFn<GetUserReturnType, GetUserDto> = (args) => {
  const { onError, onSuccess, mutationPrefixKey = [] } = args ?? {};
  const {
    isPending: isLoading,
    isError,
    error,
    data,
    mutate,
    mutateAsync,
  } = useMutation({
    mutationKey: [...mutationPrefixKey],
    mutationFn: getUser,
    onSuccess,
    onError,
  });

  return useMemo(() => {
    return { isLoading, isError, error, data, mutate, mutateAsync };
  }, [isLoading, isError, error, data, mutate, mutateAsync]);
};

